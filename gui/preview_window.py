    @staticmethod
    def _fmt_size(n: int) -> str:
        for unit in ("B", "KB", "MB", "GB"):
            if n < 1024:
                return f"{n:.1f} {unit}" if unit != "B" else f"{n} B"
            n /= 1024
        return f"{n:.1f} TB"

    @staticmethod
    def _mix(c1: str, c2: str, ratio: float = 0.4) -> str:
        try:
            r1,g1,b1 = int(c1[1:3],16), int(c1[3:5],16), int(c1[5:7],16)
            r2,g2,b2 = int(c2[1:3],16), int(c2[3:5],16), int(c2[5:7],16)
            r = int(r1*(1-ratio) + r2*ratio)
            g = int(g1*(1-ratio) + g2*ratio)
            b = int(b1*(1-ratio) + b2*ratio)
            return f"#{r:02x}{g:02x}{b:02x}"
        except Exception:
            return c1

    @staticmethod
    def _dim(color: str, factor: float) -> str:
        """Multiplica los canales RGB por factor (0-1 oscurece, >1 aclara)."""
        try:
            h = color.lstrip("#")
            r = min(255, max(0, int(int(h[0:2], 16) * factor)))
            g = min(255, max(0, int(int(h[2:4], 16) * factor)))
            b = min(255, max(0, int(int(h[4:6], 16) * factor)))
            return f"#{r:02x}{g:02x}{b:02x}"
        except Exception:
            return color
