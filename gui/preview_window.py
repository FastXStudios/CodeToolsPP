    def _load_icons(self, bg_title=None, bg_list=None, bg_footer=None):
        try:
            from PIL import Image, ImageTk
            icon_dir = resource_path("assets/icons")
            t        = self.theme_manager.get_theme()
            c_title  = bg_title  or t["secondary_bg"]
            c_list   = bg_list   or t["tree_bg"]
            c_footer = bg_footer or t["secondary_bg"]

            def _hex_to_rgb(hex_color):
                h = hex_color.lstrip("#")
                return tuple(int(h[i:i+2], 16) for i in (0, 2, 4))

            def load(name, size, bg_color):
                path = os.path.join(icon_dir, name)
                if not os.path.exists(path):
                    return None
                img = Image.open(path).convert("RGBA").resize(size, Image.Resampling.LANCZOS)
                img = self._strip_icon_matte(img)
                bg = Image.new("RGBA", img.size, _hex_to_rgb(bg_color) + (255,))
                bg.alpha_composite(img)
                return ImageTk.PhotoImage(bg.convert("RGB"))

            s16 = (16, 16)
            s13 = (13, 13)

            self._icons = {
                "preview": (load("eye.png", s16, c_title) or load("preview.png", s16, c_title)),
                "file":    load("file.png",   s13, c_list),
                "folder":  load("folder.png", s13, c_list),
                "copy":    load("copy.png",   s16, c_footer),
                "trash":   load("trash.png",  s16, c_footer),
                "close":   load("close.png",  s16, c_footer),
            }
        except ImportError:
            self._icons = {}

    def _strip_icon_matte(self, img):
        """Quita halo claro en bordes de iconos con transparencia."""
        try:
            if img.mode != "RGBA":
                img = img.convert("RGBA")

            w, h = img.size
            if w < 3 or h < 3:
                return img

            px = img.load()
            edge = []
            for x in range(w):
                edge.append(px[x, 0][:3])
                edge.append(px[x, h - 1][:3])
            for y in range(h):
                edge.append(px[0, y][:3])
                edge.append(px[w - 1, y][:3])

            if not edge:
                return img

            r = sum(c[0] for c in edge) // len(edge)
            g = sum(c[1] for c in edge) // len(edge)
            b = sum(c[2] for c in edge) // len(edge)
            # Solo aplicar cuando el matte de borde es claro.
            if (r + g + b) // 3 < 160:
                return img

            tol = 26
            q = deque()
            seen = set()

            def near_bg(xx, yy):
                pr, pg, pb, pa = px[xx, yy]
                if pa == 0:
                    return True
                return abs(pr - r) <= tol and abs(pg - g) <= tol and abs(pb - b) <= tol

            for x in range(w):
                q.append((x, 0))
                q.append((x, h - 1))
            for y in range(h):
                q.append((0, y))
                q.append((w - 1, y))

            while q:
                x, y = q.popleft()
                if (x, y) in seen:
                    continue
                seen.add((x, y))
                if x < 0 or y < 0 or x >= w or y >= h:
                    continue
                if not near_bg(x, y):
                    continue

                pr, pg, pb, _pa = px[x, y]
                px[x, y] = (pr, pg, pb, 0)
                q.append((x + 1, y))
                q.append((x - 1, y))
                q.append((x, y + 1))
                q.append((x, y - 1))

            return img
        except Exception:
            return img

